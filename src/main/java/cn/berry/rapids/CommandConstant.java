package cn.berry.rapids;

/**
 * 命令常量类
 * 
 * 描述: 定义命令行操作的常量值，包括成功、取消和错误的返回代码。
 * 这些常量用于命令执行结果的统一表示，便于程序判断命令执行状态。
 * 
 * @author Berry
 * @version 1.0.0
 */
public class CommandConstant {

    /** 命令执行成功的返回代码 */
    public static final Integer SUCCESS = 0;

    /** 命令取消的返回代码 */
    public static final Integer CANCEL = 0;

    /** 命令执行错误的返回代码 */
    public static final Integer ERROR = 0;

}
